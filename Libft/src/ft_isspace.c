#include "libft.h"

int	ft_isspace(char c)
{
	if ((c == ' ' || c == '\t' || c == '\n'
			|| c == '\v' || c == '\f' || c == '\r'))
		return (EXIT_FAILURE);
	else
		return (EXIT_SUCCESS);
}
